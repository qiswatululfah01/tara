    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $keyword = $request->keyword;
        $datas = Jadwal::where('kode_ja', 'LIKE', '%' . $keyword . '%')
            ->orwhere('nama_kegiatan', 'LIKE', '%' . $keyword . '%')
            ->orwhere('hari_kegiatan', 'LIKE', '%' . $keyword . '%')
            ->orwhere('tanggal_kegiatan', 'LIKE', '%' . $keyword . '%')
            ->simplePaginate(5);
        return view('pages.admin.jadwal.index', compact(
            'datas',
            'keyword'
        ));
    }

